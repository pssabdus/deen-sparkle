import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { message, userRole, childAge } = await req.json();
    const openAIApiKey = Deno.env.get('OPENAI_API_KEY');

    if (!openAIApiKey) {
      throw new Error('OpenAI API key not configured');
    }

    // Create appropriate system prompt based on user role
    let systemPrompt = '';
    if (userRole === 'parent') {
      systemPrompt = `You are an Islamic family assistant helping Muslim parents raise their children according to Islamic values. Provide guidance on:
      - Islamic parenting advice
      - Teaching children about Islam
      - Prayer and religious practices
      - Islamic values and character building
      - Family activities and Islamic traditions
      
      Keep responses helpful, respectful, and grounded in authentic Islamic teachings. Always encourage consulting with local Islamic scholars for detailed religious questions.`;
    } else {
      systemPrompt = `You are a friendly Islamic learning companion for Muslim children${childAge ? ` aged ${childAge}` : ''}. Help them learn about:
      - Basic Islamic teachings in simple terms
      - Stories of Prophets and Islamic history
      - Good manners and Islamic etiquette
      - Prayer and daily Islamic practices
      - Islamic values like kindness, honesty, and respect
      
      Use age-appropriate language, be encouraging, and make learning about Islam fun and engaging. Always promote good behavior and Islamic values.`;
    }

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: systemPrompt
          },
          {
            role: 'user',
            content: message
          }
        ],
        temperature: 0.7,
        max_tokens: 500,
      }),
    });

    if (!response.ok) {
      const errorData = await response.text();
      console.error('OpenAI API error:', errorData);
      throw new Error(`OpenAI API error: ${response.status}`);
    }

    const data = await response.json();
    const aiResponse = data.choices[0].message.content;

    return new Response(JSON.stringify({ 
      response: aiResponse,
      success: true 
    }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });

  } catch (error) {
    console.error('Error in islamic-ai-chat function:', error);
    return new Response(JSON.stringify({ 
      error: error.message,
      success: false 
    }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});